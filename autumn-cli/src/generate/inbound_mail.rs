    for (k, v) in FIXTURE_PAYLOAD {{
        body_parts.push(format!("{{k}}={{v}}"));
    }}
    let body = body_parts.join("&");

    build_client(key)
        .post("/inbound/mailgun")
        .form(&body)
        .send()
        .await
        .assert_status(200);

    assert_eq!(
        HANDLER_CALLS.load(Ordering::SeqCst),
        1,
        "handler must have been invoked exactly once"
    );
