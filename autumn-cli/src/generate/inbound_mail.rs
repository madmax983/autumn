/// Insert an `.inbound_mail_router(...)` call with a Mailgun endpoint into
/// the app builder chain in `src/main.rs`. Idempotent.
fn add_inbound_mail_router_to_app(src: &str, handler_module_path: &str) -> String {
    let snippet = format!(
        ".inbound_mail_router(\
            ::autumn_web::inbound_mail::InboundMailRouter::new()\
                .endpoint(::autumn_web::inbound_mail::InboundMailEndpointConfig::mailgun(\
                    \"/inbound/mailgun\",\
                    std::env::var(\"MAILGUN_SIGNING_KEY\").unwrap_or_default(),\
                ))\
                .handler({handler_module_path}())\
        )"
    );

    // If a router call already exists, chain the new handler onto it instead of inserting
    // a second router. Skip if this specific handler is already registered.
    if let Some(router_pos) = src.find(".inbound_mail_router(") {
        if src.contains(handler_module_path) {
            return src.to_owned();
        }
        // Find the closing paren of the .inbound_mail_router(...) argument.
        let start = router_pos + ".inbound_mail_router(".len();
        let bytes = src.as_bytes();
        let mut depth = 1usize;
        let mut i = start;
        while i < bytes.len() {
            match bytes[i] {
                b'(' => depth += 1,
                b')' => {
                    depth -= 1;
                    if depth == 0 {
                        // Insert before the closing `)`, but skip over any
                        // trailing comma + whitespace that rustfmt may have
                        // added after the router expression. Without this,
                        // `handler(old),)` becomes `handler(old),.handler(new))`
                        // which is invalid Rust.
                        let mut insert_pos = i;
                        while insert_pos > start && bytes[insert_pos - 1].is_ascii_whitespace() {
                            insert_pos -= 1;
                        }
                        if insert_pos > start && bytes[insert_pos - 1] == b',' {
                            insert_pos -= 1;
                        }
                        let mut out = src.to_owned();
                        out.insert_str(insert_pos, &format!(".handler({handler_module_path}())"));
                        return out;
                    }
                }
                _ => {}
            }
            i += 1;
        }
        return src.to_owned();
    }

    // Insert after `.routes(` call if present, otherwise before `.run()`.
    let target = if src.contains(".routes(") {
        // Find the closing paren of the first .routes() call and insert after
        find_after_routes_call(src).map(|pos| {
            let mut out = src.to_owned();
            out.insert_str(pos, &format!("\n        {snippet}"));
            out
        })
    } else {
        None
    };

    target.unwrap_or_else(|| {
        // Fallback: insert before `.run()`.
        src.find(".run()").map_or_else(
            || format!("{src}\n// TODO: add to app builder: {snippet}"),
            |run_pos| {
                let mut out = src.to_owned();
                out.insert_str(run_pos, &format!("{snippet}\n        "));
                out
            },
        )
    })
}

fn find_after_routes_call(src: &str) -> Option<usize> {
    let mut pos = src.find(".routes(")?;
    pos += ".routes(".len();
    let bytes = src.as_bytes();
    let mut depth = 1usize;
    while pos < bytes.len() {
        match bytes[pos] {
            b'(' => depth += 1,
            b')' => {
                depth -= 1;
                if depth == 0 {
                    return Some(pos + 1);
                }
            }
            _ => {}
        }
        pos += 1;
    }
    None
}

/// CLI entry point for `autumn generate inbound_mail <name>`.
pub fn run(name: &str, flags: Flags) {
    let cwd = match std::env::current_dir() {
        Ok(d) => d,
        Err(e) => {
            eprintln!("Error: cannot determine current directory: {e}");
            std::process::exit(1);
        }
    };
    match plan_inbound_mail(&cwd, name).and_then(|p| p.execute(flags)) {
        Ok(()) => {}
        Err(e) => {
            eprintln!("Error: {e}");
            std::process::exit(1);
        }
    }
}

